function soma(valor1, valor2){
  return valor1 + valor2
}

function subtracao(value1, value2){
  return value1-value2
}

function multiplicacao(priValor, segValor){
  return priValor * segValor
}

function divisao(priVal, segVal){
  return priVal / segVal
}

function restoDivisao (val1, val2){
  return val1 % val2
}
