const soma = (valor1, valor2) => {
  return valor1 + valor2
}

const subtracao = (value1, value2) => {
  return value1-value2
}

const multiplicacao = (priValor, segValor) => {
  return priValor * segValor
}

const divisao = (priVal, segVal) => {
  return priVal / segVal
}

const restoDivisao = (val1, val2) => {
  return val1 % val2
}
